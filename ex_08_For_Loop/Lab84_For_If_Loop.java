package ex_08_For_Loop;

public class Lab84_For_If_Loop {
    public static void main(String[] args) {
        for (int ajay = 0; ajay < 18; ajay++) {
            if(ajay > 15){
                System.out.println("Gift from Papa, iPhone 17 pro max");
            }else {
                System.out.println("No Gift");
            }
        }
    }
}
