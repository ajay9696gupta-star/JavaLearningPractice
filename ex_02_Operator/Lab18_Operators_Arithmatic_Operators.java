package ex_02_Operator;

public class Lab18_Operators_Arithmatic_Operators {
    public static void main(String[] args) {
        int a = 20, b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println("a-b");
        System.out.println(a+c);
    }
}
