package ex_02_Operator;

public class Lab22_Logical_Operator {
    public static void main(String[] args) {
        boolean a =  true;
        System.out.println(!a);

        boolean b =  true;
        System.out.println(!!a);

        boolean c =  true || false;
        System.out.println(c);

        boolean c1 = true && false;
        System.out.println(c1);
    }
}
